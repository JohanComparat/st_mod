Mock catalogs for 4MOST galaxies below redshift z<0.95.
contact: comparat@mpe.mpg.de

The Uchuu simulations (Ishiyama et al. 2021 https://ui.adsabs.harvard.edu/abs/2021MNRAS.506.4210I/abstract) are publicly available here : http://skiesanduniverses.iaa.es/

Cosmological parameters from Planck2015 are used : Ω_m = 0.3089 Ω_L = 0.6911 h = 0.6774, σ_8 = 0.8159 Ω_b = 0.0486 ns = 0.9667

It contains Rockstar/ConsistentTrees halo catalogues and the UniverseMachine galaxy evolution models (Aung, H., et al. 2022 https://ui.adsabs.harvard.edu/abs/2022MNRAS.tmp.3530A/abstract)

The light cone creation method is described in Comparat et al. 2020, https://ui.adsabs.harvard.edu/abs/2020OJAp....3E..13C/abstract

Online documentation of the mock catalogs : https://sharelatex.gwdg.de/read/fznzgdgtrgys#7a89b0

In this directoy, you will find a number of tar.gz files each corresponding to a light cone shell.

Each archive contains three files. Each files has the same number of lines. The entries in each file are in the same order. The first entry in file one corresponds to the first entry in all other files. Said differently, files can be matched by line number.

glist.fits : the simulated galaxies from Uchuu+UniverseMachine (Sec. 2.1). Theoretical galaxy population
mcen_list.fits : halo mass of the host halo (for central and satellite galaxies). Useful for HOD purpose.
zMsSFRmatch_mags.fits : absolute magnitude in the K band (Sec. 3.1) and observed magnitude (Sec. 3.2). Galaxy mock as 'observed'.

